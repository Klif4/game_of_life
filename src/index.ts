function humanReadable(cells: boolean[][]) {
    let table = new Table({
        colWidths: cells[0].map(cell => 3),
        colAligns: cells[0].map(cell => "middle"),
        colors: true
    })
    cells.forEach(cellLine => {
        table.push(cellLine.map(cell => {
            if(cell){
                return 'O'
            }
            return ''
        }))
    })
    return table.toString()

}

